//! SM4 核心加解密与密钥展开（GB/T 32907-2016 §6）
//!
//! # 安全说明
//!
//! S-box 使用**位切片（Bitslice）**实现，完全消除了缓存时序侧信道攻击面。
//! 每次 S-box 查询的访存模式与输入无关。

use zeroize::{Zeroize, ZeroizeOnDrop};

